const fruits = ["Banana", "Orange", "Apple", "Mango"];
// add item to first
fruits.unshift("Lemon", "Pineapple");
console.log(fruits);

// delete item from first
fruits.shift();
console.log(fruits);

// add item to last
fruits.push("malta");
console.log(fruits);
// delete item from last
fruits.pop("malta");
console.log(fruits);